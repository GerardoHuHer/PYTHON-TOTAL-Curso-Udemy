#Operaciones matemáticas
x = 6
y = 2
z = 7

#Suma
print(f"{x} + {y} es: {x + y}")

#Resta
print(f"{x} - {y} es: {x - y}")

#Multiplicación
print(f"{x} * {y} es: {x * y}")

#División
print(f"{x} / {y} es: {x / y}")

#División al piso, en caso de ser decimal lo trunca al entero. 7/2 = 3
print(f"{z} divido al piso de {y} es igual a {z}//{y} esto es igual a {z//y}")

#Módulo, el módulo es el residuo de la división
print(f"{x}%{y} va a ser igual {x%y}")
print(f"{z}%{y} va a ser igual {z%y}")

#Potencia
print(f"{x}**{y} es igual a {x**y}")
print(f"{x}**3 es igual a {x**3}")

#Raíz cuadrada
print(f"La raíz cuadrada de {x} es {x**0.5}")




